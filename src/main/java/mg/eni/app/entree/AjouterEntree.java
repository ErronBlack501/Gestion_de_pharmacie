    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
         try {
             
            connect();
            System.out.println((String) medocDesignComboBox.getSelectedItem());
            PreparedStatement prst1 = connection.prepareStatement("SELECT \"numMedoc\" FROM gp.\"MEDICAMENT\" WHERE design = ?");
            prst1.setString(1, (String) medocDesignComboBox.getSelectedItem());
            ResultSet result1 = prst1.executeQuery();
            String medocNumUlid = "";
            while (result1.next()) {
                medocNumUlid = result1.getString("numMedoc");
            }
            prst = connection.prepareStatement("INSERT INTO gp.\"ENTREE\" (\"medocNum\", \"stockEntree\") VALUES (CAST(? AS ulid), ?)");
            prst.setString(1, medocNumUlid);
            prst.setInt(2, Integer.parseInt(String.valueOf(stockEntreeSpinner.getValue())));
            prst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Ajout d'une nouvelle entrÃ©e rÃ©ussi!", "AJOUT D'ENTREE", 1);
            Entree.staticTableauActualisation();
            dispose();
            
        } catch (PSQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erreur de requÃªte SQL", 1);
        } catch (Exception e) {
            e.printStackTrace();
        }
        finally {
            deconnect();
        }
    }//GEN-LAST:event_jButton1MouseClicked

